def run():
    target = sys.argv[1] if len(sys.argv) > 1 else "all"
    for slug, rel, genus, atype, species_list in ENTRIES:
        if target != "all" and slug != target: continue
        p = os.path.join(ROOT, rel)
        d = load(p)
        b = count(d)
        eids = existing_ids(d)
        g = find(d["children"], genus)
        if not g:
            print(f"  {genus} not found in {slug}")
            continue
        added = 0
        for sci, common, cont in species_list:
            eid = f"{slug}_{sci.split()[-1].upper()}_BULK"
            if eid in eids:
                eid = f"{slug}_{sci.split()[-1].upper()}_BULK2"
            if eid in eids: continue
            g["children"].append(make_species(eid, sci, common, cont, DESC.get(atype, DESC["bird"])))
            eids.add(eid)
            added += 1
        a = count(d)
        save(p, d)
        print(f"{slug:20s} {b:>4} → {a:>4} (+{added})")

if __name__ == "__main__": run()
