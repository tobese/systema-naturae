def load(p):
    return json.load(open(p))
def save(p, d):
    os.makedirs(os.path.dirname(p), exist_ok=True)
    json.dump(d, open(p,"w"), indent=2, ensure_ascii=False)

def find(cc, n):
    for c in cc:
        if c.get("name") == n: return c
    return None

def count(n):
    return 1 if n.get("rank") == "SPECIES" else sum(count(c) for c in n.get("children",[]))

def existing_ids(d):
    ids = set()
    def walk(n):
        ids.add(n.get("id",""))
        for c in n.get("children",[]): walk(c)
    walk(d)
    return ids

